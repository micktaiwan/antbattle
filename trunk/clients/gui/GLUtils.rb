class GL_call_list
  
@call_list_index=Fixnum
@x=Fixnum
@debug=Fixnum
@drawed=false

  def initialize()
    @call_list_index=nil
    @x=0
    @debug=0
    @drawed=false

  end
  
def released()
  if @call_list_index==nil; true; else;  false;  end
end    

def draw()
  if @call_list_index==nil
      @call_list_index = GL.GenLists(1)
      puts "creating cl n° #{@call_list_index}" if @debug>1
      GL.NewList(@call_list_index, GL::COMPILE_AND_EXECUTE)
      yield
      GL.EndList()
  else    
   GL.CallList(@call_list_index)
  end
end    

def draw_a_time_on(x)
if @x==0 || @call_list_index==nil
  if @call_list_index!=nil
      GL.DeleteLists(@call_list_index,1)
      end
  @call_list_index = GL.GenLists(1)  
  GL.NewList(@call_list_index, GL::COMPILE_AND_EXECUTE)
  yield
  GL.EndList()
  puts @x
else
GL.CallList(@call_list_index)
end
@x=(@x+1).modulo x
end

def draw_if_undef()
  if @drawed==false
    yield
    @drawed==true
    end
end    


def release()
if @call_list_index!=nil
  GL.DeleteLists(@call_list_index,1)
  puts "destroying cl n° #{@call_list_index}" if @debug>1
  @call_list_index=nil
  end
end

end

